    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:
        int fila_seleccionada = tablacliente.getSelectedRow();

        if (fila_seleccionada >= 0) {
            modelo.removeRow(fila_seleccionada);

        } else {
            JOptionPane.showMessageDialog(null, "Por favor seleccione el cliente a eliminarðŸ™‚");
        }
    }//GEN-LAST:event_eliminarActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        this.modelo.addRow(new Object[]{this.id.getText(), this.nombre.getText(),
            this.edad.getText(), this.correo.getText(), this.telefono.getText()});

        this.id.setText("");
        this.nombre.setText("");
        this.edad.setText("");
        this.correo.setText("");
        this.telefono.setText("");
        id.grabFocus();


    }//GEN-LAST:event_guardarActionPerformed

    private void tablaclienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaclienteMouseClicked
        // TODO add your handling code here:
        int fila_seleccionada = tablacliente.getSelectedRow();
        id.setText(tablacliente.getValueAt(fila_seleccionada, 0).toString());
        nombre.setText(tablacliente.getValueAt(fila_seleccionada, 1).toString());
        edad.setText(tablacliente.getValueAt(fila_seleccionada, 2).toString());
        correo.setText(tablacliente.getValueAt(fila_seleccionada, 3).toString());
        telefono.setText(tablacliente.getValueAt(fila_seleccionada, 4).toString());

    }//GEN-LAST:event_tablaclienteMouseClicked

